#Increment and Decrement Operators
#Declare a variable 
a = 7
print(a)

#Increase its value by any value
a = a + 3
print(a)

#Increase by 
a += 4
print(a)

#Decrease by 
a -= 3
print(a)

#multiply by 
a *= 2 
print(a)

#divide by 
a /= 2
print(a)

#increase by one
a += 1
print(int(a))

#decrease by one
a -= 1
print(int(a))
